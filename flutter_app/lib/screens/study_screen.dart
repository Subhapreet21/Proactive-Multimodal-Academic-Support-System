                                    fontSize: 10, color: PdfColors.grey700))),
                      ],
                    );
                  }).toList(),
                ],
              ),
            ];
          },
        ),
      );

      await Printing.layoutPdf(
        onLayout: (PdfPageFormat format) async => pdf.save(),
        name: 'Study_Plan_${DateTime.now().millisecondsSinceEpoch}.pdf',
      );
    } catch (e) {
      if (mounted) {
        ScaffoldMessenger.of(context).showSnackBar(
          SnackBar(
            content: Text('Failed to generate PDF: $e'),
            backgroundColor: Colors.red,
            duration: const Duration(seconds: 4),
          ),
        );
      }
    }
  }

  // Mood/Energy Options
  final List<String> _energyLevels = ['Low', 'Medium', 'High'];

  Future<void> _generateStudyPlan() async {
    setState(() => _isLoading = true);

    try {
      final authProvider = Provider.of<AuthProvider>(context, listen: false);
      final token = await authProvider.getToken();
      final userId = authProvider.user?.id;
      final baseUrl = dotenv.env['API_URL'] ?? 'http://10.0.2.2:5002';

      if (userId == null) throw Exception('User not found');

      final response = await http.post(
        Uri.parse('$baseUrl/api/study/generate'),
        headers: {
          'Content-Type': 'application/json',
          'Authorization': 'Bearer $token',
        },
        body: jsonEncode({
          'userId': userId,
          'energyLevel': _selectedEnergy,
          'date': DateTime.now().toIso8601String(),
        }),
      );

      if (response.statusCode == 200) {
        setState(() {
          _studyPlan = jsonDecode(response.body);
        });
      } else {
        throw Exception('Failed to generate plan: ${response.body}');
      }
    } catch (e) {
      if (mounted) {
        ScaffoldMessenger.of(context).showSnackBar(
          SnackBar(content: Text('Error: $e'), backgroundColor: Colors.red),
        );
      }
    } finally {
      setState(() => _isLoading = false);
    }
  }

  int _parseDuration(String timeRange) {
    try {
      // Expected format: "18:00 - 19:00"
      final parts = timeRange.split('-');
      if (parts.length != 2) return 25;

      final startStr = parts[0].trim();
      final endStr = parts[1].trim();

      final start = _timeToMinutes(startStr);
      final end = _timeToMinutes(endStr);

      final diff = end - start;
      return diff > 0 ? diff : 25;
    } catch (e) {
      return 25; // Default to 25 mins if parsing fails
    }
  }

  int _timeToMinutes(String time) {
    final parts = time.split(':');
    final h = int.parse(parts[0]);
    final m = int.parse(parts[1]);
    return h * 60 + m;
  }

