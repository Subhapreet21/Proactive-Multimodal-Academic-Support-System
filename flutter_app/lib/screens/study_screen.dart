  String _stripEmojis(String text) {
    return text.replaceAll(
        RegExp(
            r'[\u{1F600}-\u{1F64F}\u{1F300}-\u{1F5FF}\u{1F680}-\u{1F6FF}\u{1F700}-\u{1F77F}\u{1F780}-\u{1F7FF}\u{1F800}-\u{1F8FF}\u{1F900}-\u{1F9FF}\u{1FA00}-\u{1FA6F}\u{1FA70}-\u{1FAFF}\u{2600}-\u{26FF}\u{2700}-\u{27BF}\u{2B50}]',
            unicode: true),
        '');
  }

  Future<void> _downloadPdf() async {
    if (_studyPlan == null) return;

    try {
      final pdf = pw.Document();
      final schedule = _studyPlan!['schedule'] as List<dynamic>;
      final rawMessage = _studyPlan!['message'] as String?;
      final message = rawMessage != null ? _stripEmojis(rawMessage) : null;

      pdf.addPage(
        pw.MultiPage(
          pageFormat: PdfPageFormat.a4,
          footer: (pw.Context context) {
            return pw.Container(
              alignment: pw.Alignment.centerRight,
              margin: const pw.EdgeInsets.only(top: 10),
              child: pw.Text(
                'Generated by Campus Assistant - Keep crushing your goals!',
                style:
                    const pw.TextStyle(color: PdfColors.grey600, fontSize: 10),
              ),
            );
          },
          build: (pw.Context context) {
            return [
              pw.Header(
                level: 0,
                child: pw.Row(
                  mainAxisAlignment: pw.MainAxisAlignment.spaceBetween,
                  children: [
                    pw.Text('Personalized Study Plan',
                        style: pw.TextStyle(
                            fontSize: 24, fontWeight: pw.FontWeight.bold)),
