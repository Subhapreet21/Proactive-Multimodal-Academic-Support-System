                          padding: const pw.EdgeInsets.all(5),
                          child: pw.Text('Tips',
                              style: pw.TextStyle(
                                  fontWeight: pw.FontWeight.bold))),
                    ],
                  ),
                  ...schedule.map((slot) {
                    final time = _stripEmojis(slot['time'] ?? '');
                    final activity = _stripEmojis(slot['activity'] ?? '');
                    final tip = _stripEmojis(slot['focus_tip'] ?? '');

                    return pw.TableRow(
                      children: [
                        pw.Padding(
                            padding: const pw.EdgeInsets.all(5),
                            child: pw.Text(time)),
                        pw.Padding(
                            padding: const pw.EdgeInsets.all(5),
                            child: pw.Text(activity)),
                        pw.Padding(
                            padding: const pw.EdgeInsets.all(5),
